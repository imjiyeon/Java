//주석 단축키 - 블럭 선택 > 컨트롤 + /
//주석 단축키 - 블럭 선택 > 컨트롤 + 시프트 + /
public class Ex01 {
	public static void main(String[] args) {
		int x; // 정수형 변수 선언
		x = 10; // x에 10을 대입

		System.out.println(x); // x를 출력하면 변수에 담긴 값이 출력됨
		System.out.println("안녕하세요"); // println함수는 출력후 줄바꿈이 실행됨
		System.out.print("반갑습니다"); // print함수는 줄바꿈안함
		System.out.print("반갑습니다");

		// sysout 작성 + 컨트롤키 + 스페이스키 
		// -> 프린트 함수가 자동완성됨
	}
}
